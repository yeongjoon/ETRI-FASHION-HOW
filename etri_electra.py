class ResnetElectraForSequenceClassification(ElectraPreTrainedModel):
    def __init__(self, config):
        super().__init__(config)
        self.num_labels = config.num_labels
        self.electra = ElectraModel(config)
        self.classifier = ElectraClassificationHead(config)

        self.init_weights()
        self.resnet = etri_resnet101()
        self.img_to_feature = nn.Linear(2048, config.hidden_size)
        self.text_and_image_classifier = nn.Sequential(
            nn.Linear(config.hidden_size * 2, config.hidden_size * 2),
            nn.Dropout(config.hidden_dropout_prob),
            nn.Tanh(),
            nn.Dropout(config.hidden_dropout_prob),
            nn.Linear(config.hidden_size * 2, 2)
        )

    def forward(
        self,
        input_ids=None,
        attention_mask=None,
        token_type_ids=None,
        position_ids=None,
        image=None, # 여기 수정함
        head_mask=None,
        inputs_embeds=None,
        labels=None,
        output_attentions=None,
        output_hidden_states=None,
        return_dict=None,
    ):
        r"""
        labels (:obj:`torch.LongTensor` of shape :obj:`(batch_size,)`, `optional`, defaults to :obj:`None`):
            Labels for computing the sequence classification/regression loss.
            Indices should be in :obj:`[0, ..., config.num_labels - 1]`.
            If :obj:`config.num_labels == 1` a regression loss is computed (Mean-Square loss),
            If :obj:`config.num_labels > 1` a classification loss is computed (Cross-Entropy).
        """
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        discriminator_hidden_states = self.electra(
            input_ids,
            attention_mask,
            token_type_ids,
            position_ids,
            head_mask,
            inputs_embeds,
            output_attentions,
            output_hidden_states,
            return_dict,
        )

        sequence_output = discriminator_hidden_states[0] # [batch_size, sequence_length, 768]
        text_feature = sequence_output

        image = self.resnet(image) # [batch_size, 2048]
        image_feature = self.img_to_feature(image).unsqueeze(2) # [batch_size, 768, 1]

        e = torch.bmm(text_feature, image_feature) # [batch_size, sequence_length, 1]

        alpha = nn.Softmax(dim=1)(e) # sequence의 토큰의 합이 1이 되도록 가중치 부여 # [batch_size, sequence_length, 1]

        text_final = torch.bmm(text_feature.transpose(1, 2), alpha).squeeze(2) # [batch_size, 768]

        final = torch.cat((text_final, image_feature.squeeze(2)), dim=-1) # [batch_size, 768*2]

        logits = self.text_and_image_classifier(final)
        # logits = self.classifier(sequence_output)

        loss = None
        if labels is not None:
            if self.num_labels == 1:
                #  We are doing regression
                loss_fct = MSELoss()
                loss = loss_fct(logits.view(-1), labels.view(-1))
            else:
                loss_fct = CrossEntropyLoss()
                loss = loss_fct(logits.view(-1, self.num_labels), labels.view(-1))

        if not return_dict:
            output = (logits,) + discriminator_hidden_states[1:]
            return ((loss,) + output) if loss is not None else output

        return SequenceClassifierOutput(
            loss=loss,
            logits=logits,
            hidden_states=discriminator_hidden_states.hidden_states,
            attentions=discriminator_hidden_states.attentions,
        )


